#include <iostream>
using namespace std;

int main()
{
  // Variables
  int age = 25;          // Integer variable
  double pi = 3.14159;   // Double variable
  char grade = 'A';      // Character variable
  bool isStudent = true; // Boolean variable

  // Constants
  const int MAX_SCORE = 100;  // Integer constant
  const double PI = 3.14;     // Double constant
  const char NEW_LINE = '\n'; // Character constant

  // Output variable values
  cout << "Age: " << age << endl;
  cout << "Pi: " << pi << endl;
  cout << "Grade: " << grade << endl;
  cout << "Is student: " << isStudent << endl;

  // Output constant values
  cout << "Max Score: " << MAX_SCORE << endl;
  cout << "Pi Constant: " << PI << endl;
  cout << "New Line Constant: " << NEW_LINE << endl;

  return 0;
}
