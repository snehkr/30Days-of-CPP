#include <iostream>
using namespace std;

int main()
{
  // int
  int a = 10;
  cout << "a = " << a << endl;
  cout << "size of a = " << sizeof(a) << endl;

  // float
  float b = 10.5;
  cout << "b = " << b << endl;
  cout << "size of b = " << sizeof(b) << endl;

  // double
  double c = 10.5;
  cout << "c = " << c << endl;
  cout << "size of c = " << sizeof(c) << endl;

  // char
  char d = 'A';
  cout << "d = " << d << endl;
  cout << "size of d = " << sizeof(d) << endl;

  // bool
  bool e = true;
  cout << "e = " << e << endl;
  cout << "size of e = " << sizeof(e) << endl;

  return 0;
}